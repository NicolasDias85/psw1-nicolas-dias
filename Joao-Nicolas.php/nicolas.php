FUNCTION GRAVARUSUARIO($CPF, $NOME, $ENDERECO, $TELEFONE)
{
    $USUARIO = ARRAY(
    $CPF => ARRAY(
    'NOME' => $NOME,
    'TELEFONE' => $TELEFONE
    'ENDERECO' => $ENDERECO,
    ));
    $USUARIO_JSON = JSON_ENCODE($USUARIO);
    $NOMEARQUIVO = 'USUARIOS.TXT';
    FILE_PUT_CONTENTS($NOMEARQUIVO, $USUARIO_JSON, FILE_APPEND);
    ECHO "SUCEFULL!";
}
